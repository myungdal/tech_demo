import MainServer;
import ServerEnginePacketAutoGenerated;

#include "Transactor_CM_REQ_ACCOUNT_USER_LIST.h"


Transactor_CM_REQ_ACCOUNT_USER_LIST::Transactor_CM_REQ_ACCOUNT_USER_LIST(
	SocketMainFromFront& frontSocket,
	CM_REQ_ACCOUNT_USER_LIST& rp
)
	:
	mFrontSocket(frontSocket),
	mRp(rp)
{
}
Result Transactor_CM_REQ_ACCOUNT_USER_LIST::OnUpdate()
{
	AccountPtr accountPtr = gAccountManager->FindOrCreateAccount(mRp.Get_account());
	if (accountPtr == nullptr)
		return Result::AUTH_ERROR;
	mAccountUserListPtr = &accountPtr->LoadAccountUserList();
	return Result::SUCCEEDED;
}
void Transactor_CM_REQ_ACCOUNT_USER_LIST::OnFinish()
{
	MainSocketUtil::SendToClient<MC_ACK_ACCOUNT_USER_LIST::Writer> wp(mFrontSocket, ACK, mRp, GetResult());
	wp.SetValues(
		mRp.Get_account()
	);
	for (const PacketKeep<ACCOUNT_USER>& accountUser : *mAccountUserListPtr)
	{
		wp.Write_accountUserList(**accountUser);
	}
}

void Transactor_CM_REQ_ACCOUNT_USER_LIST::OnError()
{
	MainSocketUtil::SendToClient<MC_ACK_ACCOUNT_USER_LIST::Writer> wp(mFrontSocket, ACK, mRp, GetResult());
	wp.SetValues(
		{}
	);
}

void Transactor_CM_REQ_ACCOUNT_USER_LIST::OnLog()
{

}

import MainServer;
import ServerEnginePacketAutoGenerated;

#include "Transactor_CM_REQ_ACCOUNT_USER_DELETE.h"


Transactor_CM_REQ_ACCOUNT_USER_DELETE::Transactor_CM_REQ_ACCOUNT_USER_DELETE(
	SocketMainFromFront& frontSocket,
	CM_REQ_ACCOUNT_USER_DELETE& rp
)
	:
	mFrontSocket(frontSocket),
	mRp(rp)
{
}
Result Transactor_CM_REQ_ACCOUNT_USER_DELETE::OnUpdate()
{
	AccountPtr accountPtr = gAccountManager->FindOrCreateAccount(mRp.Get_account());
	if (accountPtr == nullptr)
		return Result::AUTH_ERROR;
	if (false == accountPtr->DeleteAccountUser(mRp.GetHeader().GetUserId()))
		return Result::DB_ERROR;
	mAccountUserListPtr = &accountPtr->LoadAccountUserList();
	return Result::SUCCEEDED;
}
void Transactor_CM_REQ_ACCOUNT_USER_DELETE::OnFinish()
{
	MainSocketUtil::SendToClient<MC_ACK_ACCOUNT_USER_DELETE::Writer> wp(mFrontSocket, ACK, mRp, GetResult());
	for (const PacketKeep<ACCOUNT_USER>& accountUser : *mAccountUserListPtr)
	{
		wp.Write_accountUserList(**accountUser);
	}
}

void Transactor_CM_REQ_ACCOUNT_USER_DELETE::OnError()
{
	MainSocketUtil::SendToClient<MC_ACK_ACCOUNT_USER_DELETE::Writer> wp(mFrontSocket, ACK, mRp, GetResult());
}

void Transactor_CM_REQ_ACCOUNT_USER_DELETE::OnLog()
{

}

Transactor_CM_REQ_GAME_LIST_OVER::Transactor_CM_REQ_GAME_LIST_OVER(
	SocketMainFromFront& frontSocket,
	CM_REQ_GAME_LIST_OVER& rp
)
	:
	mFrontSocket(frontSocket),
	mRp(rp)
{
}

Result Transactor_CM_REQ_GAME_LIST_OVER::OnUpdate()
{
	return Result::SUCCEEDED;
}

void Transactor_CM_REQ_GAME_LIST_OVER::OnFinish()
{
	const GameMap& gameMap = gGameListManager->GetGameList();
	const int64_t totalCount = static_cast<int64_t>(gameMap.size());
	
	MainSocketUtil::SendToClient<MC_ACK_GAME_LIST_OVER::Writer> wp(mFrontSocket, ACK, mRp, GetResult());
	wp.SetValues(
		totalCount
	);
	int64_t count = 0;
	auto it = gameMap.upper_bound(mRp.Get_overGameId());
	while (it != gameMap.end() && count < MAX_COUNT_PER_GAME_LIST_PGAE)
	{
		const GamePtr& gamePtr = it->second;
		const GAME& game = gamePtr->GetGame();
		wp.Write_gameList(game); // 패킷에 추가
		++count;

		++it;
	}
}


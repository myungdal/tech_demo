#include "MmoSync/System/Skill/SkillSystemElectric.h"

#include "Common/Stat/statContainer/StatContainer.h"


SkillSystemElectric::SkillSystemElectric(Engine& engine, TimeStepUpdateCycle timeStepUpdateCycle, GameInstance& game)
	:
	SkillSystemBase(engine, timeStepUpdateCycle, game)
{
	IncludeComponent<SkillComponentElectric>();
	IncludeComponent<TacticsComponentCombat>();
	ExcludeComponent<MotionComponentGroupA>();
	ExcludeComponent<MotionComponentGroupB>();
	ExcludeComponent<MotionComponentGroupC>();
}
void SkillSystemElectric::Update()
{
	UpdateNormalSkill<SkillComponentElectric, MotionComponentSkillA>();
}

#pragma once


////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// BadWordType - 금지어 노드 타입
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// 트라이 노드의 단어 종료 상태를 나타내는 열거형.
// - NONE: 중간 노드 (이 위치에서 끝나는 단어 없음)
// - BLACK: 블랙리스트 단어의 끝 (검증 시 실패 반환)
// - WHITE: 화이트리스트 단어의 끝 (블랙리스트보다 우선, 검증 통과)
//
// 우선순위: WHITE > BLACK > NONE
// 예: "assemble"을 WHITE로, "ass"를 BLACK으로 등록하면
//     "assemble" 입력 시 WHITE가 우선하여 통과
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

enum class BadWordType : uint8_t
{
	NONE = 0,   // 중간 노드 (단어 종료 아님)
	BLACK,      // 블랙리스트 (검증 실패)
	WHITE       // 화이트리스트 (검증 통과, BLACK보다 우선)
};

struct BadWordNode;
using BadWordNodeMap = std::unordered_map<wchar_t, BadWordNode*>;  // 문자 → 다음 노드 매핑
